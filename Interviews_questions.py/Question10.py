# What are the different file-processing modes with Python?
# ANSWER
# We have the following modes-

# read-only – ‘r’
# write-only – ‘w’
# read-write – ‘rw’
# append – ‘a’
# We can open a text file with the option ‘t’. So to open a text file to read it, we can use the mode ‘rt’. Similarly,
#  for binary files, we use ‘b’